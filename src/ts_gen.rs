    #[test]
    fn generate_api_get_with_query() {
        let content = generate_api_file("GetUserInfo", "getUserInfo", true, false, false);
        assert!(
            content.contains("import spec from \"bindings/endpoints/GetUserInfo\";"),
            "default spec import"
        );
        assert!(
            content.contains("import type { GetUserInfoQuery, GetUserInfoResponse }"),
            "type imports include only non-never role types"
        );
        // GetUserInfoBody and GetUserInfoPathParams are absent roles; `never` is emitted
        // directly in the callEndpoint<> type params so no import is needed.
        assert!(
            !content.contains("import type { GetUserInfoBody"),
            "body alias must not appear in the import type statement"
        );
        assert!(
            !content.contains("import type { GetUserInfoPathParams"),
            "pathParams alias must not appear in the import type statement"
        );
        assert!(
            content.contains("export async function getUserInfo("),
            "function exported with camelCase name"
        );
        assert!(
            content.contains("query?: GetUserInfoQuery,"),
            "optional query parameter"
        );
        assert!(
            content.contains("): Promise<ApiResult<GetUserInfoResponse>> {"),
            "return type wraps Response in ApiResult"
        );
        assert!(
            content.contains("import type { ApiResult } from \"bindings/ApiResult\";"),
            "ApiResult imported from bindings"
        );
        // Absent roles use `never` directly in type params (not the alias name) so no extra import needed.
        assert!(
            content.contains("callEndpoint<GetUserInfoQuery, never, never, GetUserInfoResponse>"),
            "explicit type params: absent roles use never, not alias names"
        );
        assert!(
            content.contains("spec, { query }"),
            "query arg passed to callEndpoint"
        );
    }

