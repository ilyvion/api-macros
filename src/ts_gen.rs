//! Convert extracted Rust types to TypeScript type strings and generate `.ts` file content.

use std::collections::BTreeSet;
use std::fmt::Write as _;

use syn::{GenericArgument, PathArguments, Type, TypePath, TypeTuple};

use crate::type_extract::ExtractedRole;

// ---------------------------------------------------------------------------
// Public entry point
// ---------------------------------------------------------------------------

/// Generate the full TypeScript file content for one endpoint.
///
/// Returns the file content as a `String`, or a [`syn::Error`] if the function signature
/// contains a type that cannot be converted to TypeScript.
pub(crate) fn generate_ts_file(
    ts_name: &str,
    method: &str,
    path: &str,
    query: Option<&Type>,
    body: Option<&Type>,
    path_params: Option<&Type>,
    response: &ExtractedRole,
) -> syn::Result<String> {
    let mut imports: BTreeSet<String> = BTreeSet::new();

    let query_ts = query.map_or_else(
        || Ok("never".to_owned()),
        |t| rust_type_to_ts(t, &mut imports),
    )?;
    let body_ts = body.map_or_else(
        || Ok("never".to_owned()),
        |t| rust_type_to_ts(t, &mut imports),
    )?;
    let path_params_ts = path_params.map_or_else(
        || Ok("never".to_owned()),
        |t| rust_type_to_ts(t, &mut imports),
    )?;
    let response_ts = match response {
        ExtractedRole::Single(t) => rust_type_to_ts(t, &mut imports)?,
        ExtractedRole::Union(a, b) => {
            let a_ts = rust_type_to_ts(a, &mut imports)?;
            let b_ts = rust_type_to_ts(b, &mut imports)?;
            format!("{a_ts} | {b_ts}")
        }
    };

    // TypeScript doesn't allow `import type { X }` and `export type X = X` to share the same
    // local name. Detect the conflict: when a role alias would equal the imported type name
    // (e.g. `export type FooBody = FooBody`), we use a re-export form instead:
    //   `export type { FooBody } from '../FooBody';`
    // That form creates no local binding and therefore never conflicts.
    let roles = [
        (format!("{ts_name}Query"), &query_ts),
        (format!("{ts_name}Body"), &body_ts),
        (format!("{ts_name}PathParams"), &path_params_ts),
        (format!("{ts_name}Response"), &response_ts),
    ];
    let mut re_exports: BTreeSet<String> = BTreeSet::new();
    for (alias, ts_type) in &roles {
        if alias == ts_type.as_str() && imports.contains(ts_type.as_str()) {
            imports.remove(ts_type.as_str());
            re_exports.insert((*ts_type).clone());
        }
    }

    let mut out = String::new();
    out.push_str(
        "// This file was generated by the api_endpoint macro. Do not edit this file manually.\n",
    );

    if !imports.is_empty() {
        out.push('\n');
        for import in &imports {
            let _ = writeln!(out, "import type {{ {import} }} from \"../{import}\";");
        }
    }
    // EndpointSpec is a phantom-typed descriptor consumed by callEndpoint in http.ts.
    let _ = writeln!(
        out,
        "import type {{ EndpointSpec }} from \"client/services/http\";"
    );

    let _ = write!(
        out,
        "\nexport const {ts_name}Method = \"{method}\" as const;"
    );
    let _ = writeln!(out, "\nexport const {ts_name}Path   = \"{path}\" as const;");

    for (alias, ts_type) in &roles {
        if re_exports.contains(ts_type.as_str()) {
            // No local binding — use re-export to avoid TS2440.
            let _ = writeln!(out, "export type {{ {ts_type} }} from \"../{ts_type}\";");
        } else {
            let _ = writeln!(out, "export type  {alias} = {ts_type};");
        }
    }

    // Typed spec object — `satisfies` validates at compile time without unsafe casting.
    // Role aliases ({ts_name}Query, {ts_name}Body, etc.) are used as the phantom type
    // params so this always compiles even when they resolve to `never`.
    let _ = writeln!(
        out,
        "\nexport default {{\n    \
             method: {ts_name}Method,\n    \
             path: {ts_name}Path,\n\
         }} satisfies EndpointSpec<\n    \
             {ts_name}Query,\n    \
             {ts_name}Body,\n    \
             {ts_name}PathParams,\n    \
             {ts_name}Response\n\
         >;",
    );

    Ok(out)
}

/// Generate the content of `generated/api/<TsName>.ts` — a thin typed wrapper
/// that calls `callEndpoint` with the endpoint's spec and the appropriate arguments.
///
/// Parameters:
/// - `ts_name`         — PascalCase endpoint name (e.g. `"GetUserInfo"`).
/// - `fn_name`         — camelCase TypeScript function name (e.g. `"getUserInfo"`).
/// - `has_query`       — whether the endpoint has a query-string type (not `never`).
/// - `has_body`        — whether the endpoint has a request-body type (not `never`).
/// - `has_path_params` — whether the endpoint has path-parameter types (not `never`).
pub(crate) fn generate_api_file(
    ts_name: &str,
    fn_name: &str,
    has_query: bool,
    has_body: bool,
    has_path_params: bool,
) -> String {
    let mut out = String::new();
    out.push_str(
        "// This file was generated by the api_endpoint macro. Do not edit this file manually.\n",
    );

    // Default import for the spec; local alias `spec` is used in the callEndpoint call.
    let _ = writeln!(out, "\nimport spec from \"bindings/endpoints/{ts_name}\";");

    // Type-only imports for parameter and return-type annotations.
    let mut type_imports: Vec<String> = Vec::new();
    if has_query {
        type_imports.push(format!("{ts_name}Query"));
    }
    if has_body {
        type_imports.push(format!("{ts_name}Body"));
    }
    if has_path_params {
        type_imports.push(format!("{ts_name}PathParams"));
    }
    type_imports.push(format!("{ts_name}Response")); // always needed for the return type
    let _ = writeln!(
        out,
        "import type {{ {} }} from \"bindings/endpoints/{ts_name}\";",
        type_imports.join(", ")
    );
    // ApiResult wraps every response; callEndpoint now returns Promise<ApiResult<R>>.
    let _ = writeln!(
        out,
        "import type {{ ApiResult }} from \"bindings/ApiResult\";"
    );

    // callEndpoint only throws on network/parse failures; HTTP errors are in ApiResult.success.
    let _ = writeln!(
        out,
        "import {{ callEndpoint }} from \"client/services/http\";"
    );

    // Function signature — required params first (pathParams, body), optional last (query).
    let _ = writeln!(out, "\nexport async function {fn_name}(");
    if has_path_params {
        let _ = writeln!(out, "    pathParams: {ts_name}PathParams,");
    }
    if has_body {
        let _ = writeln!(out, "    body: {ts_name}Body,");
    }
    if has_query {
        let _ = writeln!(out, "    query?: {ts_name}Query,");
    }
    let _ = writeln!(out, "): Promise<ApiResult<{ts_name}Response>> {{");

    // Explicit type parameters avoid phantom-type inference ambiguity: TypeScript cannot
    // infer Q/B/P/R from the spec value alone because EndpointSpec's type params are phantom.
    // Use `never` directly for absent roles so we avoid importing names just for type params.
    let query_tp = if has_query {
        format!("{ts_name}Query")
    } else {
        "never".to_owned()
    };
    let body_tp = if has_body {
        format!("{ts_name}Body")
    } else {
        "never".to_owned()
    };
    let path_params_tp = if has_path_params {
        format!("{ts_name}PathParams")
    } else {
        "never".to_owned()
    };
    let explicit_type_params =
        format!("{query_tp}, {body_tp}, {path_params_tp}, {ts_name}Response");

    let mut call_args: Vec<&str> = Vec::new();
    if has_query {
        call_args.push("query");
    }
    if has_body {
        call_args.push("body");
    }
    if has_path_params {
        call_args.push("pathParams");
    }

    if call_args.is_empty() {
        let _ = writeln!(
            out,
            "    return callEndpoint<{explicit_type_params}>(spec);"
        );
    } else {
        let _ = writeln!(
            out,
            "    return callEndpoint<{explicit_type_params}>(spec, {{ {} }});",
            call_args.join(", ")
        );
    }
    out.push_str("}\n");

    out
}

