    #[test]
    fn imports_are_sorted_and_deduped() {
        // Use the same named type twice (in body and response) → one import
        let body = parse_type("SharedType");
        let response = ExtractedRole::Single(Box::new(parse_type("AnotherType")));
        let content = generate_ts_file(
            "TestDedup",
            "POST",
            "test/dedup",
            None,
            Some(&body),
            None,
            &response,
        )
        .unwrap();
        let import_lines: Vec<&str> = content
            .lines()
            .filter(|l| l.starts_with("import type"))
            .collect();
        // AnotherType and SharedType come from the BTreeSet (sorted); EndpointSpec is always last
        assert_eq!(import_lines.len(), 3);
        assert!(import_lines[0].contains("AnotherType"));
        assert!(import_lines[1].contains("SharedType"));
        assert!(import_lines[2].contains("EndpointSpec"));
    }
